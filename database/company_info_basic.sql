CREATE TABLE company_info_basic(
         id integer PRIMARY KEY AUTOINCREMENT,
         name VARCHAR,
         stock_code VARCHAR,
         used_abbr TEXT,
         abbr TEXT,
         indices TEXT,
         industry TEXT,
         setup_date VARCHAR,
         listing_date VARCHAR,
         website VARCHAR,
         email VARCHAR,
         main_business TEXT
         business_scope TEXT,
         introduction TEXT
      );
